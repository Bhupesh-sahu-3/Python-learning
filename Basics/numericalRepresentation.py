print(0o123) # octal representation of 123, converts to 83 in decimal
print(0x123) # hexadecimal representation of 123, converts to 291 in decimal
print(0b1010)  # binary representation of 10, converts to 10 in decimal
print(3e4) # scientific notation, converts to 30000.0 in decimal i.e. 3*10^4
print(3e-4) # scientific notation, converts to 0.0003 in decimal i.e. 3*10^-4


